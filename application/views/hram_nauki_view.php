<h2>У входа в храм науки о случайном</h2>
<img src="/images/hram_nauki_1.jpg" width="100%" height="7%" hspace="3"><br>
    Однажды на страницах журнала «Современник» (1836), издаваемого А.С. Пушкиным, появилась статья «О надежде», написанная князем П.Б. Козловским — дипломатом, глубоко образованным человеком. Статья начиналась с эпиграфа из Горация:<br><br>

    <i>Осветить истину сквозь туман заблуждений</i><br><br>

    — и представляла собой первое в русской литературе популярное изложение, как пишет автор,<br><br>

    <i>философической математики, называемой исчислением вероятностей или — по-моему лучше — наукой исчисления удобосбытностей</i>.<br><br>

    Идея числовой оценки шансов на случайный успех, на выигрыш, на появление ожидаемого события «стара как мир», но обретать силу метода, теории она начала с середины XVII века в трудах Паскаля (1623–1662), Ферма (1601–1665), Гюйгенса (1629–1695), Я. Бернулли (1654–1705). Мощный импульс развитию теории вероятностей дали ставшие классическими работы Чебышева (1821–1894), главы созданной им «петербургской» школы, Маркова (1856–1922), Ляпунова (1857–1918).<br><br>

    Статус истинно математической науки теория вероятностей обрела лишь с 1933 года, когда Колмогоровым (1903–1987) был создан аксиоматический фундамент теории — введено аксиоматическое определение вероятности, принятое всюду и поныне. Теория вероятностей теперь — это величественный храм мощной, фундаментальной, очень красивой науки.<br>
<hr width="50%"><br>
В преддверии этого храма понятие вероятности формируется вначале на абстрактной модели реальных испытаний с известным числом n случайных исходов, предполагаемых равновозможными. Обозначим их Е<sub>1</sub>, Е<sub>2</sub>, ..., Е<sub>n</sub>. Так, в модели испытаний, состоящих в бросании монеты, предусматриваются только два возможных исхода:
<br><br>
Е1 — появление герба,
<br><br>
Е2 — появление решки.
<br><br>
Оба исхода предполагаются (если к этому есть объективные основания) равновозможными, и потому естественно считать их равновероятными.
<br>
Любое возможное множество исходов называется <i>случайным событием</i>. Множество, состоящее из всех n возможных исходов рассматриваемых испытаний, образует <i>достоверное событие</i>. Достоверному событию приписывается вероятность, равная 1. Когда все n исходов, образующих достоверное событие, признаются равновозможными, их считают равновероятными; тогда вероятность р каждого отдельного исхода равна <sup>1</sup>/<sub>n</sub>, то есть
<br>
р = <sup>1</sup>/<sub>n</sub>.
<br>
Если известно, что m исходов из n возможных образуют событие А, то его вероятность
<br>
Р (А) = <sup>m</sup>/<sub>n</sub>
<br>
или в форме классического определения вероятности:
<br>
<i>вероятность события А равна отношению числа m — исходов, благоприятствующих наступлению события A, к числу n — всех равновозможных исходов</i>.
<br>
Для случайных событий А постулируются три основных утверждения:
<br><br>
1. 0 ≤ Р (A) ≤ 1.
<br><br>
2. Р (А) = 1, если A — <i>достоверное событие</i>.
<br><br>
3. Р (А∪В) = Р (А) + Р (В), <i>где событие А∪В означает осуществление или события А, или события В, причем А и В не могут произойти одновременно</i>.
<br><br>
