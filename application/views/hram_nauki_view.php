<h2>Задача №1. Бросание монеты</h2>
<hr width="50%">
Найдем вероятность события: при трехразовом бросании монеты появляется герб хотя бы один раз (событие А).<br>

Решение.<br>

Для выяснения количества всех возможных исходов удобна следующая таблица.<br><br>
<img src="/images/hram_nauki_2.jpg" hspace="3" height="20%" width="70%"><br><br>
Только в одном из восьми исходов герб не появляется ни разу — в событии РРР. В остальных семи случаях герб появляется хотя бы один раз. Следовательно,<br><br>

Р (А) = <sup>7</sup>/<sub>8</sub> = 0,875.
<br>
Ответ: 0,875.
<hr width="50%">
Наряду с событием А∪В, что означает: <i>или</i> A <i>или</i> В, вводят еще событие А∩B, что означает: совместное появление событий <i>и</i> А <i>и</i> В одновременно или одно после другого. Если при этом реальные условия для событий А и В таковы, что вероятность одного из них, скажем В, характеризуется одним и тем же числом вне зависимости от того, произошло или нет событие А, то такие события называются <i>независимыми</i>. Имеет место <i>теорема умножения</i>:

<i>если события</i> А <i>и</i> В <i>независимы, то</i>

Р (А∩B) = Р (А) · Р (В),

<i>то есть вероятность совместного появления независимых событий</i> А <i>и</i> В <i>равна произведению вероятностей</i>  Р (А)  <i>и</i>  Р (В).
<br><br>
